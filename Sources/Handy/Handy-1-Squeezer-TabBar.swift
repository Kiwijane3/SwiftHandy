public extension TabBarProtocol {
    /// Connect a Swift signal handler to the given, typed `TabBarSignalName` signal
    /// - Parameters:
    ///   - signal: The signal to connect
    ///   - flags: The connection flags to use
    ///   - data: A pointer to user data to provide to the callback
    ///   - destroyData: A `GClosureNotify` C function to destroy the data pointed to by `userData`
    ///   - handler: The Swift signal handler (function or callback) to invoke on the given signal
    /// - Returns: The signal handler ID (always greater than 0 for successful connections)
    @inlinable @discardableResult func connect(signal s: TabBarSignalName, flags f: ConnectFlags = ConnectFlags(0), handler h: @escaping SignalHandler) -> Int {
        connect(s, flags: f, handler: h)
    }
    
    
    /// Connect a C signal handler to the given, typed `TabBarSignalName` signal
    /// - Parameters:
    ///   - signal: The signal to connect
    ///   - flags: The connection flags to use
    ///   - data: A pointer to user data to provide to the callback
    ///   - destroyData: A `GClosureNotify` C function to destroy the data pointed to by `userData`
    ///   - signalHandler: The C function to be called on the given signal
    /// - Returns: The signal handler ID (always greater than 0 for successful connections)
    @inlinable @discardableResult func connect(signal s: TabBarSignalName, flags f: ConnectFlags = ConnectFlags(0), data userData: gpointer!, destroyData destructor: GClosureNotify? = nil, signalHandler h: @escaping GCallback) -> Int {
        connectSignal(s, flags: f, data: userData, destroyData: destructor, handler: h)
    }
    
    
    /// This signal is emitted when content allowed via
    /// `HdyTabBar:extra-drag-dest-targets` is dropped onto a tab.
    /// 
    /// See `GtkWidget::drag-data-received`.
    /// - Note: This represents the underlying `extra-drag-data-received` signal
    /// - Parameter flags: Flags
    /// - Parameter unownedSelf: Reference to instance of self
    /// - Parameter page: the `HdyTabPage` matching the tab the content was dropped onto
    /// - Parameter context: the drag context
    /// - Parameter data: the received data
    /// - Parameter info: the info that has been registered with the target in the `GtkTargetList`
    /// - Parameter time: the timestamp at which the data was received
    /// - Parameter handler: The signal handler to call
    /// Run the given callback whenever the `extraDragDataReceived` signal is emitted
    @discardableResult @inlinable func onExtraDragDataReceived(flags: ConnectFlags = ConnectFlags(0), handler: @escaping ( _ unownedSelf: TabBarRef, _ page: TabPageRef, _ context: Gdk.DragContextRef, _ data: Gtk.SelectionDataRef, _ info: UInt, _ time: UInt) -> Void ) -> Int {
        typealias SwiftHandler = GLib.ClosureHolder6<TabBarRef, TabPageRef, Gdk.DragContextRef, Gtk.SelectionDataRef, UInt, UInt, Void>
        let cCallback: @convention(c) (gpointer, gpointer, gpointer, gpointer, guint, guint, gpointer) -> Void = { unownedSelf, arg1, arg2, arg3, arg4, arg5, userData in
            let holder = Unmanaged<SwiftHandler>.fromOpaque(userData).takeUnretainedValue()
            let output: Void = holder.call(TabBarRef(raw: unownedSelf), TabPageRef(raw: arg1), Gdk.DragContextRef(raw: arg2), Gtk.SelectionDataRef(raw: arg3), UInt(arg4), UInt(arg5))
            return output
        }
        return connect(
            signal: .extraDragDataReceived,
            flags: flags,
            data: Unmanaged.passRetained(SwiftHandler(handler)).toOpaque(),
            destroyData: { userData, _ in UnsafeRawPointer(userData).flatMap(Unmanaged<SwiftHandler>.fromOpaque(_:))?.release() },
            signalHandler: unsafeBitCast(cCallback, to: GCallback.self)
        )
    }
    
    /// Typed `extra-drag-data-received` signal for using the `connect(signal:)` methods
    static var extraDragDataReceivedSignal: TabBarSignalName { .extraDragDataReceived }
    
